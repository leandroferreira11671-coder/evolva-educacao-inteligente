function abrirAula(aula){

    if(aula === "fonologia"){
        window.location.href = "basico/fonologia.html"
    }

    if(aula === "morfologia"){
        window.location.href = "basico/morfologia.html"
    }

    if(aula === "sintaxe"){
        window.location.href = "basico/sintaxe.html"
    }

    if(aula === "semantica"){
        window.location.href = "basico/semantica.html"
    }

}
